class Cat:
    def move(self):
        print("Cat prowls silently 🐱")

class Fish:
    def move(self):
        print("Fish swims smoothly 🐟")

class Bird:
    def move(self):
        print("Bird soars high 🦅")

# Create instances
cat = Cat()
fish = Fish()
bird = Bird()

# Call their move methods
cat.move()   # Cat prowls silently 🐱
fish.move()  # Fish swims smoothly 🐟
bird.move()  # Bird soars high 🦅
